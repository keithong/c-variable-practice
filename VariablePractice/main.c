int main(int argc, const char * argv[])
{
    int x = 19;
    float y = 1.9;
    char z = 'a';
    
    printf("The value of x is %d\n", x);
    printf("The value of y is %.2f\n", y);
    printf("The character inside z is %c\n",z);
    
    return 0;
}

